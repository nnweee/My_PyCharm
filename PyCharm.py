# Ввод строки от пользователя
input_str = input("Введите строку: ")

# Преобразуем строку в массив символов
char_array = list(input_str)

# Переменные для подсчета
removed_count = 0
result_array = []

# Проходим по каждому символу
for char in char_array:
    if char == ':':
        # Если символ двоеточие - увеличиваем счетчик
        removed_count += 1
    else:
        # Иначе добавляем символ в новый массив
        result_array.append(char)

# Преобразуем результат обратно в строку
result_str = ''.join(result_array)

# Вывод результатов
print(f"Исходная строка: {input_str}")
print(f"Строка после удаления двоеточий: {result_str}")
print(f"Количество удаленных символов ':': {removed_count}")
