# Ввод 11 символов
print("Введите 11 символов: ")
input_str = input()[:11]  # Берем первые 11 символов

# Массив символов
char_array = list(input_str)

# Преобразование строчных букв в верхний регистр
result = [char.upper() if char.islower() else char for char in char_array]

# Вывод
print(f"Исходно: {input_str}")
print(f"Результат: {''.join(result)}")
